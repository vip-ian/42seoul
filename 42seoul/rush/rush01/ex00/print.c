#include <unistd.h>

void	print(char matrix[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			write(1, &matrix[i][j], 1);
			if (j != 3)
				write(1, " ", 1);
			else if (j == 3)
				write(1, "\n", 1);
			j++;
		}
		i++;		
	}
}
