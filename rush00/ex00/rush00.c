void	ft_putchar(char *a);

void	rush(int x, int y)
{
	int	i[2];

	i[0] = 1;
	i[1] = 1;
	while (i[0] <= y)
	{
		while (i[1] <= x)
		{
			if (((i[1] == 1) && (i[0] == 1)) || ((i[1] == x) && (i[0] == y)))
				ft_putchar("o");
			else if ((i[1] == 1 && i[0] == y) || (i[1] == x && i[0] == 1))
				ft_putchar("o");
			else if ((i[1] == 1 && i[0] != y) || (i[1] == x && i[0] != 1))
				ft_putchar("|");
			else if ((i[0] == 1 && i[1] != 1) || (i[0] == y && i[1] != x))
				ft_putchar("-");
			else
				ft_putchar(" ");
			i[1]++;
		}
		i[0]++;
		i[1] = 1;
		ft_putchar("\n");
	}
}
