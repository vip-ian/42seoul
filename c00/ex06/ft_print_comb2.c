#include <unistd.h>
#include <string.h>

void	ft_print_comb2(void)
{
	int		a[2];
	char	b[7];

	a[0] = 00;
	a[1] = 01;
	b[2] = ' ';
	b[5] = ',';
	b[6] = ' ';
	while (a[0] <= 98)
	{
		while (a[1] <= 99)
		{
			b[0] = '0' + (a[0] / 10);
			b[1] = '0' + (a[0] % 10);
			b[3] = '0' + (a[1] / 10);
			b[4] = '0' + (a[1] % 10);
			if (a[0] == 98 && a[1] == 99)
				write(1, b, 5);
			else
				write(1, b, 7);
			a[1]++;
		}
		a[0]++;
		a[1] = a[0] + 1;
	}
}
