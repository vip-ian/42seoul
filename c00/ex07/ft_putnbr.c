#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	a;
	char	b;

	a = '0';
	b = '-';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb *= -1;
		write(1, &b, 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb <= 9)
	{
		a += nb;
		write(1, &a, 1);
	}
}
