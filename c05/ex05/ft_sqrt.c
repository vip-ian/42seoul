int	ft_rc(int nb, int i)
{
	if (nb > 2147395600)
		return (0);
	if (i * i == nb)
		return (i);
	if (i * i < nb)
		return (ft_rc(nb, i + 1));
	return (0);
}

int	ft_sqrt(int nb)
{
	return (ft_rc(nb, 0));
}
