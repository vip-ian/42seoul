char	*ft_strlowcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (*str != 0)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str += 32;
		str++;
	}
	str = tmp;
	return (str);
}
