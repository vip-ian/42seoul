#include <stdio.h>

char	*ft_strupcase(char *str)
{
	char	*temp;

	temp = str;
	while (*str != 0)
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= 32;
		str++;
	}
	str = temp;
	return (str);
}
